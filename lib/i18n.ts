const TRANSLATIONS: Record<AppLocale, TranslationTree> = {
  en: EN_TRANSLATIONS,
  es: ES_TRANSLATIONS,
  "pt-BR": PT_TRANSLATIONS,
  fr: FR_TRANSLATIONS,
  kn: KN_TRANSLATIONS,
  ta: TA_TRANSLATIONS,
}

const readTranslationValue = (tree: TranslationTree, key: string): string | null => {
  const parts = key.split(".")
  let current: TranslationValue | undefined = tree
  for (const part of parts) {
    if (!current || typeof current === "string") return null
    current = current[part]
  }
  return typeof current === "string" ? current : null
}

export const normalizeAppLocale = (value: unknown, fallback: AppLocale = DEFAULT_APP_LOCALE): AppLocale => {
  const locale = String(value || "").trim()
  if (!locale) return fallback
  const lower = locale.toLowerCase()
  if (lower.startsWith("pt")) return "pt-BR"
  if (lower.startsWith("es")) return "es"
  if (lower.startsWith("fr")) return "fr"
  if (lower.startsWith("kn")) return "kn"
  if (lower.startsWith("ta")) return "ta"
  return "en"
}

export const getLocaleLabel = (locale: AppLocale) =>
  APP_LOCALES.find((option) => option.code === locale)?.label || APP_LOCALES[0].label

export const translate = (localeInput: unknown, key: string, values?: Record<string, string | number>) => {
  const locale = normalizeAppLocale(localeInput)
  const template =
    readTranslationValue(TRANSLATIONS[locale], key) ??
    readTranslationValue(TRANSLATIONS.en, key) ??
    key

  if (!values) {
    return template
  }

  return Object.entries(values).reduce(
    (result, [token, value]) => result.replaceAll(`{{${token}}}`, String(value)),
    template,
  )
}
